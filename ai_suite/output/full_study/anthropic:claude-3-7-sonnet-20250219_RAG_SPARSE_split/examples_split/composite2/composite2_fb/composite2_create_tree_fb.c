struct node *create_tree()
    //@ requires true;
    //@ ensures tree(result);
{
    struct node *n = malloc(sizeof(struct node));
    if (n == 0) {
        abort();
    }
    n->left = 0;
    n->right = 0;
    n->parent = 0;
    n->count = 1;
    
    //@ close subtree(0, n, 0);
    //@ close subtree(0, n, 0);
    //@ close subtree(n, 0, 1);
    //@ close context(n, 0, 1);
    //@ close tree(n);
    
    return n;
}